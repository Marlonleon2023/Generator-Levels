// ===== FUNCIÓN PRINCIPAL PARA CAMBIAR IDIOMA =====
function changeLanguage(lang) {
    localStorage.setItem('language', lang);
    window._currentLang = lang;

    const currentLangSpan = document.getElementById('currentLanguage');
    if (currentLangSpan) {
        currentLangSpan.textContent = lang === 'es' ? '🌐 ES' : '🌐 EN';
    }

    // Texto normal
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang]?.[key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[lang]?.[key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Values de inputs (solo si el usuario no los ha modificado)
    document.querySelectorAll('[data-translate-value]').forEach(element => {
        const key = element.getAttribute('data-translate-value');
        if (translations[lang]?.[key]) {
            const defaultEs = translations['es'][key];
            const defaultEn = translations['en'][key];
            if (element.value === defaultEs || element.value === defaultEn || element.value === '') {
                element.value = translations[lang][key];
            }
        }
    });

    // Options de selects
    document.querySelectorAll('option[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang]?.[key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Marcar item activo en dropdown
    document.querySelectorAll('.language-selector .dropdown-item').forEach(item => {
        item.classList.remove('active');
        const onclick = item.getAttribute('onclick');
        if (onclick && onclick.includes(`'${lang}'`)) {
            item.classList.add('active');
        }
    });

    document.documentElement.lang = lang;
}

// ===== MODO OSCURO =====
function toggleDarkMode() {
    const html = document.documentElement;
    const isDark = html.getAttribute('data-theme') === 'dark';
    if (isDark) {
        html.removeAttribute('data-theme');
        localStorage.setItem('darkMode', 'false');
    } else {
        html.setAttribute('data-theme', 'dark');
        localStorage.setItem('darkMode', 'true');
    }
    updateDarkModeIcon();
}

function updateDarkModeIcon() {
    const icon = document.getElementById('darkModeIcon');
    if (!icon) return;
    const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
    icon.className = isDark ? 'bi bi-sun-fill' : 'bi bi-moon-fill';
    const btn = document.getElementById('darkModeToggle');
    if (btn) {
        btn.title = isDark ? 'Modo Claro' : 'Modo Oscuro';
    }
}

function initDarkMode() {
    const saved = localStorage.getItem('darkMode');
    if (saved === 'true') {
        document.documentElement.setAttribute('data-theme', 'dark');
    }
    updateDarkModeIcon();
}

